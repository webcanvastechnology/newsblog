											<p>In a huge labyrinthine ecosystem of labels, publishers, distributors, and royalty collectors, the idea of artists getting paid quickly may sound like a revolutionary concept. What if it wasn’t?

What if the music industry’s hundreds of thousands of artists — who are especially cash-strapped when they can’t tour, like right now in the Covid-19 pandemic — didn’t have to wait a cripplingly long time to get their royalties, but rather could receive streaming money instantaneously? Broaden that thought experiment: What if there was a “merch royalty,” so that the artist got a cut every time a branded hoodie or piece of art was resold, or a system to ensure accurate paychecks for musicians?

All these things are possible, according to music’s cryptocurrency enthusiasts — a growing group of tech-obsessed entrepreneurs who want to apply the technology of blockchain and bitcoin to the multibillion-dollar global business of selling albums. Crypto advocates want to engineer a new financial system for music that can support real-time revenue streams and ultimately hand much more power to the individual artist. The average music fan may be easily deterred by these conversations: Cryptocurrency is, well, cryptic, and articles on the subject tend to be filled with in-the-know jargon and lengthy digressions. So Rolling Stone has scoped out the landscape and, with the help of experts like Zach Katz, Shara Senderoff, Adam Alpert, and RAC, we’ll walk you through the genesis, potential applications, and latest developments in what crypto can mean for music.</p>
